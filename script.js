const addNums = function(num1, num2) {
    return num1 + num2;
}

const subtractNums = function(num1, num2) {
    return num1 - num2;
}

const multiplyNums = function(num1, num2) {
    return num1 * num2;
}

const divideNums = function(num1, num2) {
    return num1 / num2;
}

console.log(addNums(8,4));
console.log(subtractNums(8,4));
console.log(multiplyNums(8,4));
console.log(divideNums(8,4));